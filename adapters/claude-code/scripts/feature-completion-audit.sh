#!/bin/bash
# feature-completion-audit.sh — periodic post-fact backstop for the
# eight-criteria feature-completion definition (ADR 046 / rules/completion-criteria.md).
#
# WHY THIS EXISTS
# ===============
# completion-criteria-gate.sh is the fast IN-SESSION backstop: it fires when a
# session DECLARES a feature shipped and checks the eight criteria against the
# session's own final message. But sessions can phrase a shipment unusually
# (slipping the gate's phrase trigger) or skip-flag criteria. This script is
# the thorough POST-FACT net: it walks features that are ALREADY marked
# COMPLETED and verifies each against the same eight criteria using real
# repository state — git log, docs/support/*.mdx presence, page-registry
# classification, migration / deploy markers — independent of any session's
# self-report.
#
# PROJECT-GENERIC BY DESIGN
# =========================
# This script ships in the harness kit (adapters/), scanned by the hygiene
# gate, so it carries NO project identifiers. It is parameterized by
# --project <path> and keys off CONVENTIONS, not names:
#   <project>/docs/plans/**/*.md   with `Status: COMPLETED`   (the features)
#   <project>/docs/support/*.mdx                              (user docs)
#   <project>/src/lib/page-registry.ts (optional)            (contractor-facing
#                                                             classification:
#                                                             owner:'org' + doc_path)
#   <project>/docs/backlog.md (optional)                     (deploy/migration markers)
#
# THE EIGHT CRITERIA (keys match completion-criteria-gate.sh)
#   code tests dev_docs user_docs migration deploy acceptance stakeholder
#
# Most criteria are HEURISTIC from a bash/grep audit (no gh/supabase needed —
# the script runs cold). The ONE hard signal is page-registry doc coverage:
# an owner:'org' (contractor-facing) page whose doc_path file does not exist is
# an unambiguous user-docs GAP. That section is the load-bearing finding.
#
# USAGE
#   feature-completion-audit.sh [--project <path>] [--out <path>] [--date YYYY-MM-DD]
#                               [--page-registry <path>] [--quiet]
#   feature-completion-audit.sh --self-test
#
#   --project        repo root to audit (default: current directory)
#   --out            report path (default: <project>/docs/audits/completion-audit-<date>.md)
#   --date           report date stamp (default: today, UTC)
#   --page-registry  page-registry file (default: <project>/src/lib/page-registry.ts if present)
#   --quiet          suppress the stdout summary (report file is still written)
#
# EXIT CODES
#   0  audit ran, no hard gaps found (or nothing COMPLETED to audit)
#   1  audit ran, >=1 hard gap found (contractor-facing page missing its doc,
#      or a completed user-facing plan with no user-doc reference)
#   2  usage error (bad flag, unreadable project)

set -u

# ------------------------------------------------------------
# Arg parsing
# ------------------------------------------------------------
PROJECT="."
OUT=""
DATE=""
PAGE_REGISTRY=""
QUIET=0

if [[ "${1:-}" == "--self-test" ]]; then
  SELFTEST=1
else
  SELFTEST=0
  while [[ $# -gt 0 ]]; do
    case "$1" in
      --project)       PROJECT="${2:-}"; shift 2 ;;
      --out)           OUT="${2:-}"; shift 2 ;;
      --date)          DATE="${2:-}"; shift 2 ;;
      --page-registry) PAGE_REGISTRY="${2:-}"; shift 2 ;;
      --quiet)         QUIET=1; shift ;;
      -h|--help)
        sed -n '2,40p' "${BASH_SOURCE[0]}" | sed 's/^# \{0,1\}//'
        exit 0 ;;
      *) echo "feature-completion-audit: unknown arg '$1'" >&2; exit 2 ;;
    esac
  done
fi

# ------------------------------------------------------------
# Core audit. Echoes the report to stdout; sets HARD_GAPS count via a file
# (so it works under subshell-free calling). Returns 0 (clean) / 1 (gaps).
# Args: $1 = project root, $2 = date stamp, $3 = page-registry path (or "")
# ------------------------------------------------------------
run_audit() {
  local proj="$1" date_stamp="$2" preg="$3"
  local plans_dir="$proj/docs/plans"
  local support_dir="$proj/docs/support"
  local backlog="$proj/docs/backlog.md"
  local hard_gaps=0
  local completed_count=0

  # Resolve default page-registry if not supplied.
  if [[ -z "$preg" ]] && [[ -f "$proj/src/lib/page-registry.ts" ]]; then
    preg="$proj/src/lib/page-registry.ts"
  fi

  echo "# Feature-Completion Audit — $date_stamp"
  echo ""
  echo "Generated by \`feature-completion-audit.sh\` (ADR 046). Verifies features"
  echo "already marked \`Status: COMPLETED\` against the eight completion criteria"
  echo "using real repository state. Heuristic criteria are marked \`~\`; the"
  echo "page-registry doc-coverage section is a hard file-existence check."
  echo ""
  echo "Project audited: \`$(basename "$proj")\`"
  echo ""

  # ============================================================
  # Section A — Completed-plan eight-criteria heuristic audit
  # ============================================================
  echo "## A. Completed plans — eight-criteria heuristics"
  echo ""
  if [[ ! -d "$plans_dir" ]]; then
    echo "_No \`docs/plans/\` directory found — nothing to audit._"
    echo ""
  else
    # Gather COMPLETED plans (top-level + archive).
    local plan_files=()
    while IFS= read -r f; do
      [[ -z "$f" ]] && continue
      if grep -qiE '^[[:space:]]*Status:[[:space:]]*COMPLETED' "$f" 2>/dev/null; then
        plan_files+=("$f")
      fi
    done < <(find "$plans_dir" -maxdepth 2 -name '*.md' -type f 2>/dev/null | sort)

    if [[ ${#plan_files[@]} -eq 0 ]]; then
      echo "_No \`Status: COMPLETED\` plans found._"
      echo ""
    else
      echo "| Feature (plan) | code | tests | dev_docs | user_docs | migration | deploy | acceptance | Gaps |"
      echo "|---|:--:|:--:|:--:|:--:|:--:|:--:|:--:|---|"
      local pf
      for pf in "${plan_files[@]}"; do
        completed_count=$((completed_count+1))
        local id; id="$(basename "$pf" .md)"
        # Read plan body once (+ sibling evidence if present).
        local body; body="$(cat "$pf" 2>/dev/null)"
        local ev="${pf%.md}-evidence.md"
        [[ -f "$ev" ]] && body="$body"$'\n'"$(cat "$ev" 2>/dev/null)"

        local gaps=""

        # code — merge / PR reference present?
        local code_m="~"
        if printf '%s' "$body" | grep -qiE 'merged|squash-merged|PR #?[0-9]+|commit [0-9a-f]{7}' 2>/dev/null; then code_m="ok"; else code_m="?"; fi

        # tests
        local tests_m="~"
        if printf '%s' "$body" | grep -qiE '\btest|\bspec|e2e|playwright|vitest|jest' 2>/dev/null; then tests_m="ok"; else tests_m="?"; gaps="${gaps}tests "; fi

        # dev_docs
        local devd_m="~"
        if printf '%s' "$body" | grep -qiE 'docs/decisions/|\bADR\b|runbook|architecture' 2>/dev/null; then devd_m="ok"; else devd_m="?"; fi

        # user-facing? (STRICT: an explicit contractor / support / navigable-page
        # cue. A loose path-token match over-flags every plan, so we require an
        # explicit signal here and leave the HARD user-docs verdict to Section B.)
        local userfacing=0
        if printf '%s' "$body" | grep -qiE 'docs/support/|contractor-facing|support page|user-facing|user-doc|dashboard page|nav(igation)? (item|page)|/support/' 2>/dev/null; then userfacing=1; fi

        # user_docs — references a support doc? Advisory `review`, NOT a hard gap:
        # the authoritative user-docs signal is the page-registry file-existence
        # check in Section B. Per-plan heuristics are conservative prompts to
        # confirm, not confirmed gaps (a plan can ship a doc without naming it).
        local userd_m="n/a"
        if [[ "$userfacing" -eq 1 ]]; then
          if printf '%s' "$body" | grep -qiE 'docs/support/[a-z0-9-]+\.mdx' 2>/dev/null; then
            userd_m="ok"
          else
            userd_m="review"; gaps="${gaps}user_docs? "
          fi
        fi

        # migration
        local mig_m="n/a"
        if printf '%s' "$body" | grep -qiE 'migration|supabase/migrations|schema change' 2>/dev/null; then
          if printf '%s' "$body" | grep -qiE 'pushed to prod|applied|verified applied|migration [0-9]+ (pushed|applied|verified)' 2>/dev/null; then
            mig_m="ok"
          else
            mig_m="?"; gaps="${gaps}migration-applied? "
          fi
        fi

        # deploy
        local dep_m="~"
        if printf '%s' "$body" | grep -qiE 'deploy[a-z]* (green|success|succeeded|verified)|production deploy|DEPLOYED' 2>/dev/null; then dep_m="ok"; else dep_m="?"; fi

        # acceptance
        local acc_m="~"
        if printf '%s' "$body" | grep -qiE 'acceptance|smoke|task-verifier|evidence|verified' 2>/dev/null; then acc_m="ok"; else acc_m="?"; fi

        [[ -z "$gaps" ]] && gaps="—"
        echo "| \`$id\` | $code_m | $tests_m | $devd_m | $userd_m | $mig_m | $dep_m | $acc_m | ${gaps% } |"
      done
      echo ""
      echo "_Legend: \`ok\` = signal found · \`?\` = signal absent (review) · \`~\` = heuristic · \`n/a\` = not applicable · \`GAP\` = hard gap._"
      echo ""
    fi
  fi

  # ============================================================
  # Section B — Contractor-facing page doc coverage (HARD check)
  # ============================================================
  echo "## B. Contractor-facing page documentation coverage (hard check)"
  echo ""
  if [[ -z "$preg" ]] || [[ ! -f "$preg" ]]; then
    echo "_No page-registry found (looked for \`src/lib/page-registry.ts\` or \`--page-registry\`)._"
    echo "_Skipping the hard doc-coverage check; Section A heuristics still apply._"
    echo ""
  else
    echo "Page registry: \`$(basename "$preg")\`. Each \`owner: 'org'\` (contractor-facing)"
    echo "page must have its \`doc_path\` support doc on disk."
    echo ""
    # Parse the registry into per-entry blocks. Fields can appear in ANY order
    # within an object (real registries list doc_path before owner), so collect
    # all fields and emit at the closing `}` of each object.
    # awk emits: slug|route|owner|doc_path per entry.
    local entries
    entries="$(awk '
      function pick(line,   s) {
        match(line, /:[[:space:]]*[\x27"][^\x27"]+[\x27"]/);
        s=substr(line,RSTART,RLENGTH); sub(/:[[:space:]]*[\x27"]/,"",s); sub(/[\x27"].*/,"",s);
        return s;
      }
      /^[[:space:]]*\{/ { slug=""; route=""; owner=""; doc="" }
      /[[:space:]]*slug:[[:space:]]*[\x27"]/     { slug=pick($0) }
      /[[:space:]]*route:[[:space:]]*[\x27"]/    { route=pick($0) }
      /[[:space:]]*owner:[[:space:]]*[\x27"]/    { owner=pick($0) }
      /[[:space:]]*doc_path:[[:space:]]*[\x27"]/ { doc=pick($0) }
      /^[[:space:]]*\},?[[:space:]]*$/ { if (slug!="") { print slug "|" route "|" owner "|" doc; slug="" } }
    ' "$preg" 2>/dev/null)"

    # Org (contractor-facing) pages — HARD gap if the doc file is missing.
    echo "### B.1 Contractor-facing (\`owner: 'org'\`) pages — missing doc = hard gap"
    echo ""
    echo "| Page (slug/route) | doc_path | exists? |"
    echo "|---|---|:--:|"
    local org_total=0 org_missing=0
    while IFS='|' read -r slug route owner doc; do
      [[ -z "$slug" ]] && continue
      [[ "$owner" != "org" ]] && continue
      org_total=$((org_total+1))
      local exists="yes"
      if [[ -z "$doc" ]] || [[ ! -f "$proj/$doc" ]]; then
        exists="**NO**"; org_missing=$((org_missing+1)); hard_gaps=$((hard_gaps+1))
      fi
      echo "| \`$slug\` (${route:-?}) | \`${doc:-—}\` | $exists |"
    done <<< "$entries"
    echo ""
    if [[ "$org_total" -eq 0 ]]; then
      echo "_No \`owner: 'org'\` entries parsed from the registry._"
    else
      echo "Contractor-facing pages: $org_total · **missing docs: $org_missing** (each = a hard gap)."
    fi
    echo ""

    # Platform / admin pages — advisory (the platform operator is a user too, but
    # criterion #4's strict bar is contractor-facing). List only those missing docs.
    echo "### B.2 Platform / admin pages missing docs — advisory"
    echo ""
    local adv_lines="" adv_count=0
    while IFS='|' read -r slug route owner doc; do
      [[ -z "$slug" ]] && continue
      [[ "$owner" == "org" ]] && continue
      if [[ -z "$doc" ]] || [[ ! -f "$proj/$doc" ]]; then
        adv_lines="${adv_lines}| \`$slug\` (${route:-?}) | \`${doc:-—}\` | ${owner:-?} |"$'\n'
        adv_count=$((adv_count+1))
      fi
    done <<< "$entries"
    if [[ "$adv_count" -eq 0 ]]; then
      echo "_All platform/admin pages with a registry entry have their doc on disk._"
    else
      echo "| Page (slug/route) | doc_path | owner |"
      echo "|---|---|:--:|"
      printf '%s' "$adv_lines"
    fi
    echo ""
  fi

  # ============================================================
  # Summary
  # ============================================================
  echo "## Summary"
  echo ""
  echo "- Completed plans audited (Section A): $completed_count"
  echo "- Hard gaps found (user-doc GAPs + missing contractor-facing docs): **$hard_gaps**"
  echo ""
  echo "Notes:"
  echo "- \`stakeholder\` notification is not verifiable from repo state alone and is omitted from the per-plan table; verify it manually for user-facing shipments."
  echo "- A \`?\` is a prompt to confirm, not a confirmed gap — the heuristics are conservative."
  echo "- File catch-up backlog items for each hard gap (Section A \`GAP\` columns + Section B \`NO\` rows) so the remediation is tracked."
  echo ""

  # Record the gap count for the caller.
  printf '%s' "$hard_gaps" > "${AUDIT_GAPCOUNT_FILE:-/dev/null}"
  if [[ "$hard_gaps" -gt 0 ]]; then return 1; fi
  return 0
}

# ============================================================
# --self-test
# ============================================================
if [[ "$SELFTEST" -eq 1 ]]; then
  PASS=0; FAIL=0
  check() { if [[ "$1" == "$2" ]]; then echo "PASS  $3"; PASS=$((PASS+1)); else echo "FAIL  $3 (expected '$2' got '$1')"; FAIL=$((FAIL+1)); fi; }

  TROOT=$(mktemp -d 2>/dev/null || mktemp -d -t fca)

  # --- Scenario 1: completed user-facing plan WITH a support doc -> clean ---
  P1="$TROOT/p1"; mkdir -p "$P1/docs/plans" "$P1/docs/support"
  cat > "$P1/docs/plans/feature-a.md" <<'EOF'
# Plan: Feature A
Status: COMPLETED
Goal: add the /widgets dashboard page. PR #10 merged, commit abc1234.
Tests added in e2e/widgets.spec.ts. User docs at docs/support/widgets.mdx.
Deploy green. Acceptance verified via smoke test.
EOF
  : > "$P1/docs/support/widgets.mdx"
  out1="$(AUDIT_GAPCOUNT_FILE="$TROOT/g1" run_audit "$P1" "2026-06-01" ""; echo "rc=$?")"
  rc1="$(printf '%s' "$out1" | tail -1)"
  check "$rc1" "rc=0" "completed-plan-with-support-doc -> clean (exit 0)"

  # --- Scenario 2: completed user-facing plan WITHOUT a support doc -> gap ---
  P2="$TROOT/p2"; mkdir -p "$P2/docs/plans" "$P2/docs/support"
  cat > "$P2/docs/plans/feature-b.md" <<'EOF'
# Plan: Feature B
Status: COMPLETED
Goal: add the /transfer dashboard page and button. PR #11 merged.
EOF
  out2="$(AUDIT_GAPCOUNT_FILE="$TROOT/g2" run_audit "$P2" "2026-06-01" ""; echo "rc=$?")"
  rc2="$(printf '%s' "$out2" | tail -1)"
  # No page-registry here, so the user-docs signal is heuristic-only -> advisory
  # `user_docs?` (a prompt to confirm), NOT a hard gap. Hard gaps come from B.
  check "$rc2" "rc=0" "completed-user-facing-no-doc -> advisory review (exit 0, not hard gap)"
  if printf '%s' "$out2" | grep -q 'user_docs?'; then echo "PASS  advisory review flagged"; PASS=$((PASS+1)); else echo "FAIL  advisory review missing"; FAIL=$((FAIL+1)); fi

  # --- Scenario 3: no completed plans -> clean ---
  P3="$TROOT/p3"; mkdir -p "$P3/docs/plans"
  cat > "$P3/docs/plans/feature-c.md" <<'EOF'
# Plan: Feature C
Status: ACTIVE
EOF
  out3="$(AUDIT_GAPCOUNT_FILE="$TROOT/g3" run_audit "$P3" "2026-06-01" ""; echo "rc=$?")"
  rc3="$(printf '%s' "$out3" | tail -1)"
  check "$rc3" "rc=0" "no-completed-plans -> clean (exit 0)"

  # --- Scenario 4: missing docs/plans dir -> graceful clean ---
  P4="$TROOT/p4"; mkdir -p "$P4"
  out4="$(AUDIT_GAPCOUNT_FILE="$TROOT/g4" run_audit "$P4" "2026-06-01" ""; echo "rc=$?")"
  rc4="$(printf '%s' "$out4" | tail -1)"
  check "$rc4" "rc=0" "missing-docs-plans -> graceful clean (exit 0)"
  if printf '%s' "$out4" | grep -q 'nothing to audit'; then echo "PASS  graceful message present"; PASS=$((PASS+1)); else echo "FAIL  graceful message missing"; FAIL=$((FAIL+1)); fi

  # --- Scenario 5: page-registry hard check — owner:'org' missing doc -> gap ---
  P5="$TROOT/p5"; mkdir -p "$P5/docs/plans" "$P5/docs/support" "$P5/src/lib"
  cat > "$P5/src/lib/page-registry.ts" <<'EOF'
const PAGES: readonly PageEntry[] = [
  {
    slug: 'contacts',
    route: '/contacts',
    doc_path: 'docs/support/contacts.mdx',
    owner: 'org',
    nav_label: 'Contacts',
  },
  {
    slug: 'console',
    route: '/admin/console',
    doc_path: 'docs/support/console.mdx',
    owner: 'platform',
    nav_label: 'Console',
  },
  {
    slug: 'transfer',
    route: '/transfer',
    doc_path: 'docs/support/transfer.mdx',
    owner: 'org',
    nav_label: 'Transfer',
  },
];
EOF
  : > "$P5/docs/support/contacts.mdx"   # transfer.mdx intentionally absent
  out5="$(AUDIT_GAPCOUNT_FILE="$TROOT/g5" run_audit "$P5" "2026-06-01" ""; echo "rc=$?")"
  rc5="$(printf '%s' "$out5" | tail -1)"
  check "$rc5" "rc=1" "page-registry org-page-missing-doc -> gap (exit 1)"
  # contacts present -> yes ; transfer absent -> NO ; console is platform -> not counted
  if printf '%s' "$out5" | grep -qE '\bcontacts\b.*yes'; then echo "PASS  contacts shown present"; PASS=$((PASS+1)); else echo "FAIL  contacts not shown present"; FAIL=$((FAIL+1)); fi
  if printf '%s' "$out5" | grep -qE '\btransfer\b.*NO'; then echo "PASS  transfer flagged missing"; PASS=$((PASS+1)); else echo "FAIL  transfer not flagged"; FAIL=$((FAIL+1)); fi
  b1_5="$(printf '%s' "$out5" | awk '/### B.1/{f=1} /### B.2/{f=0} f')"
  if printf '%s' "$b1_5" | grep -qE '\bconsole\b'; then echo "FAIL  platform page leaked into B.1 org check"; FAIL=$((FAIL+1)); else echo "PASS  platform page excluded from B.1 org check"; PASS=$((PASS+1)); fi
  if printf '%s' "$out5" | grep -qE 'B.2|advisory' && printf '%s' "$out5" | awk '/### B.2/{f=1} f' | grep -qE '\bconsole\b'; then echo "PASS  platform page surfaced in B.2 advisory"; PASS=$((PASS+1)); else echo "FAIL  platform page not in B.2 advisory"; FAIL=$((FAIL+1)); fi

  # --- Scenario 6: page-registry all org docs present -> clean ---
  : > "$P5/docs/support/transfer.mdx"
  out6="$(AUDIT_GAPCOUNT_FILE="$TROOT/g6" run_audit "$P5" "2026-06-01" ""; echo "rc=$?")"
  rc6="$(printf '%s' "$out6" | tail -1)"
  # Note: the completed-plan section has no plans here, so the only gap source is page-registry; now clean.
  check "$rc6" "rc=0" "page-registry all-org-docs-present -> clean (exit 0)"

  rm -rf "$TROOT"
  echo ""
  echo "self-test: $PASS pass, $FAIL fail"
  if [[ "$FAIL" -gt 0 ]]; then echo "self-test: FAIL"; exit 1; fi
  echo "self-test: OK $PASS/$PASS"
  exit 0
fi

# ============================================================
# Normal path
# ============================================================
if [[ ! -d "$PROJECT" ]]; then
  echo "feature-completion-audit: --project '$PROJECT' is not a directory" >&2
  exit 2
fi
PROJECT="$(cd "$PROJECT" && pwd)"

if [[ -z "$DATE" ]]; then
  DATE="$(date -u '+%Y-%m-%d' 2>/dev/null || date '+%Y-%m-%d')"
fi
if [[ -z "$OUT" ]]; then
  OUT="$PROJECT/docs/audits/completion-audit-$DATE.md"
fi

mkdir -p "$(dirname "$OUT")" 2>/dev/null || true

GAPFILE="$(mktemp 2>/dev/null || echo "$PROJECT/.completion-audit-gapcount")"
AUDIT_GAPCOUNT_FILE="$GAPFILE" run_audit "$PROJECT" "$DATE" "$PAGE_REGISTRY" > "$OUT"
RC=$?
GAPS="$(cat "$GAPFILE" 2>/dev/null || echo 0)"
rm -f "$GAPFILE" 2>/dev/null || true

if [[ "$QUIET" -ne 1 ]]; then
  echo "feature-completion-audit: report written to $OUT"
  echo "feature-completion-audit: hard gaps found: ${GAPS:-0}"
  if [[ "$RC" -ne 0 ]]; then
    echo "feature-completion-audit: GAPS PRESENT — file catch-up backlog items for each (see report)." >&2
  fi
fi
exit "$RC"
